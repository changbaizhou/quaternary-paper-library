const thresholds = {
  themes: 2,
  regions: 2,
  periods: 2,
  materials: 2,
  methods: 1,
  proxies: 1
};

function normalize(value) {
  return String(value || "").normalize("NFKC").toLowerCase();
}

function collectEvidence(alias, sources) {
  const evidence = [];
  let score = 0;
  const needle = normalize(alias);
  for (const source of sources) {
    if (source.text.includes(needle)) {
      evidence.push(alias);
      score += source.weight;
    }
  }
  return { score, evidence };
}

export function classifyText({ title = "", abstract = "", keywords = [], text = "" } = {}) {
  const sources = [
    { name: "title", text: normalize(title), weight: 3 },
    { name: "keywords", text: normalize(keywords.join(" ")), weight: 3 },
    { name: "abstract", text: normalize(abstract), weight: 1 },
    { name: "text", text: normalize(text.slice(0, 2000)), weight: 0.4 }
  ];
  const classification = {};
  const confidence = {};
  const evidence = {};

  for (const [dimension, entries] of Object.entries(taxonomy)) {
    classification[dimension] = [];
    confidence[dimension] = {};
    evidence[dimension] = {};

    for (const [label, aliases] of entries) {
      let score = 0;
      const matches = [];
      for (const alias of aliases) {
        const result = collectEvidence(alias, sources);
        score += result.score;
        matches.push(...result.evidence);
      }

      if (score >= thresholds[dimension]) {
        classification[dimension].push(label);
        confidence[dimension][label] = Math.min(0.98, 0.45 + score * 0.1);
        evidence[dimension][label] = [...new Set(matches)];
      }
    }
  }

  return { classification, confidence, evidence };
}

export function getTaxonomy() {
  return taxonomy;
}
